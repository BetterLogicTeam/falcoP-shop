// Helper functions for filtering products
export const getProductsByCategory = (category: 'men' | 'women' | 'kids') => {
  return products.filter(product => product.category === category)
}

export const getProductsBySubcategory = (subcategory: 'sportswear' | 'shoes') => {
  return products.filter(product => product.subcategory === subcategory)
}

export const getProductsByType = (type: string) => {
  return products.filter(product => product.type === type)
}

export const getFeaturedProducts = () => {
  return products.filter(product => product.badge && ['Best Seller', 'Popular', 'Pro Choice'].includes(product.badge))
}

export const getNewProducts = () => {
  return products.filter(product => product.badge === 'New')
}

export const getSaleProducts = () => {
  return products.filter(product => product.originalPrice && product.originalPrice > product.price)
}
